// given my location on gps, pick a random point that has direct line from me
function getRandomLocation(fromx, fromz, minDistane, maxDistance) {
    var found = false;
    while (!found) {
        var r = GameInfo.fieldRadius * (0.3 + 0.5 * Math.random());
        var ang = Math.PI * 2 * Math.random();
        var x = r * Math.cos(ang);
        var z = r * Math.sin(ang);
        found = true;
        // make sure location is far from from x/z
        var dist = Math.sqrt( (x-fromx)*(x-fromx) + (z-fromz)*(z-fromz) );
        if (dist < minDistane) continue;
        if (dist > maxDistance) continue;
        
        // make sure point is not inside any silo
        for (var k=0; k<GameInfo.silolist.length; k++) {
            var s = GameInfo.silolist[k];
            var dist = Math.sqrt( (s.x-fromx)*(s.x-fromx) + (s.z-fromz)*(s.z-fromz) );
            if (dist < s.rad + cfg.fieldRadius * 0.03) {
                found = false;
                break;
            }
        }
    }
    return {x:x, z:z};
}

function hasClearPath(N, angle) {
    var myInfo = carInfo[GameInfo.playerID];

    // extend next N steps along direction of angle and see if any silo blocker or car blocker
    var lineAngle = myInfo.gpsa + angle;

    for(var step=0; step < N; step ++) {
        var travelDistance = myInfo.speed / 60 * step;
        var newx = myInfo.gpsx + travelDistance * Math.cos(lineAngle);
        var newz = myInfo.gpsz + travelDistance * Math.sin(lineAngle);

        var isInsideSilo = false;
        for(var j=0; j<GameInfo.silolist.length; j++) {
            var silo = GameInfo.silolist[j];
            var dist = distance(silo.x, silo.z, newx, newz);
            if (dist <= silo.rad) {
                isInsideSilo = true;
                break;
            }
        }

        if (isInsideSilo) return false;
    }

    return true;
}




